def test_generate_empty_graph_produces_stub_content(
    neo4j_driver, project_dir, domain_config, clean_test_db, tmp_path
):
    """With no artifacts, generated files contain 'No X artifacts' stubs."""
    out_dir = tmp_path / "docs"
    _generate_docs(neo4j_driver, NEO4J_DB, domain_config, out_dir)

    assert "No Script artifacts in graph" in (out_dir / "scripts_reference.md").read_text()
    assert "No Result artifacts in graph" in (out_dir / "results_registry.md").read_text()
